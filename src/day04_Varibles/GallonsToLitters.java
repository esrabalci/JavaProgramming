package day04_Varibles;

public class GallonsToLitters {

    public static void main(String[] args) {

        // 1 Gallon = 3.79 Litres

        int gallon = 1000;
        double litres = gallon * 3.79;

        System.out.println("gallon = " + gallon);
        System.out.println("litres = " + litres);









    }

}

