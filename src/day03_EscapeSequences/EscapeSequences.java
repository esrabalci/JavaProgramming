package day03_EscapeSequences; // it's package name of the class
/*
escape sequences: Must be given with double quote
    \n: starts a new line
    \t: paragraph space(tab)
    \\: single back slash
    \" : double quote



 */
public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#"); // \nNAME gives a new line insteaf of the writing more lines of long version

        System.out.println("-----------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("______________________________");

        System.out.println("\tJava is Cool Programming Language");
        System.out.println("______________________________");

        System.out.println("/ \\"); // in order to print one backward slash we need to give two backward slashes

        System.out.println("______________________________");

        System.out.println("My favorite TV show is \"Friends\""); // in order to print double quote you need to use double quote end ot the sentences either







    }





}
